;-------------------------------------------------------------------------------
include '../include/library.inc'
;-------------------------------------------------------------------------------

library 'USBDRVCE', 0

;-------------------------------------------------------------------------------
; no dependencies
;-------------------------------------------------------------------------------

;-------------------------------------------------------------------------------
; v0 functions (not final, subject to change!)
;-------------------------------------------------------------------------------
	export usb_Init
	export usb_Cleanup
;-------------------------------------------------------------------------------

;-------------------------------------------------------------------------------
usbRangeLow		= $D13FD8
usbRangeHigh		= $D177BC
;-------------------------------------------------------------------------------

;-------------------------------------------------------------------------------
usb_Init:
	ret
usb_Cleanup:
	ld	hl,mpUsbCmd
	ld	(hl),h
	ld	hl,usbRangeLow
	ld	bc,usbRangeHigh-usbRangeLow
	jp	_MemClear
;-------------------------------------------------------------------------------
